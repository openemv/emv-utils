static int populate_tlv_list(
	const struct emv_tlv_t* tlv_array,
	size_t tlv_array_count,
	struct emv_tlv_list_t* list
)
{
	int r;

	emv_tlv_list_clear(list);
	for (size_t i = 0; i < tlv_array_count; ++i) {
		r = emv_tlv_list_push(list, tlv_array[i].tag, tlv_array[i].length, tlv_array[i].value, 0);
		if (r) {
			return r;
		}
	}

	return 0;
}

int main(void)
{
	int r;
	const struct emv_capk_t* capk;
	struct emv_tlv_list_t valid_icc = EMV_TLV_LIST_INIT;
	struct emv_tlv_list_t valid_params = EMV_TLV_LIST_INIT;
	struct emv_rsa_issuer_pkey_t ipk;
	struct emv_rsa_ssad_t ssad;
	struct emv_rsa_icc_pkey_t icc_pkey;
	struct emv_rsa_sdad_t sdad;

	// Enable debug output
	r = emv_debug_init(EMV_DEBUG_SOURCE_ALL, EMV_DEBUG_LEVEL_ALL, &print_emv_debug);
	if (r) {
		fprintf(stderr, "emv_debug_init() failed; r=%d\n", r);
		return 1;
	}

	r = emv_capk_init();
	if (r) {
		fprintf(stderr, "emv_capk_init() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	capk = emv_capk_lookup(test_capk_rid, test_capk_index);
	if (!capk) {
		fprintf(stderr, "emv_capk_lookup() failed\n");
		r = 1;
		goto exit;
	}

	// Populate fields required for certificate validation
	r = populate_tlv_list(
		valid_icc_data,
		sizeof(valid_icc_data) / sizeof(valid_icc_data[0]),
		&valid_icc
	);
	if (r) {
		fprintf(stderr, "populate_tlv_list() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	r = populate_tlv_list(
		valid_params_data,
		sizeof(valid_params_data) / sizeof(valid_params_data[0]),
		&valid_params
	);
	if (r) {
		fprintf(stderr, "populate_tlv_list() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}

	// Test retrieval of issuer public key with validation
	memset(&ipk, 0, sizeof(ipk));
	r = emv_rsa_retrieve_issuer_pkey(
		valid_issuer_cert,
		sizeof(valid_issuer_cert),
		capk,
		&valid_icc,
		&valid_params,
		&ipk
	);
	if (r) {
		fprintf(stderr, "emv_rsa_retrieve_issuer_pkey() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&ipk, &full_issuer_pkey_verify, sizeof(full_issuer_pkey_verify)) != 0) {
		fprintf(stderr, "Retrieved issuer public key is incorrect\n");
		print_buf("retrieved", &ipk, sizeof(ipk));
		print_buf("expected", &full_issuer_pkey_verify, sizeof(full_issuer_pkey_verify));
		r = 1;
		goto exit;
	}

	// Test retrieval of ICC public key with certificate validation but no ODA
	memset(&icc_pkey, 0, sizeof(icc_pkey));
	r = emv_rsa_retrieve_icc_pkey(
		valid_icc_cert,
		sizeof(valid_icc_cert),
		&ipk,
		&valid_icc,
		&valid_params,
		NULL,
		&icc_pkey
	);
	if (r != 10) {
		fprintf(stderr, "emv_rsa_retrieve_icc_pkey() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&icc_pkey, &full_icc_pkey_verify, sizeof(full_icc_pkey_verify)) != 0) {
		fprintf(stderr, "Retrieved ICC public key is incorrect\n");
		print_buf("retrieved", &icc_pkey, sizeof(icc_pkey));
		print_buf("expected", &full_icc_pkey_verify, sizeof(full_icc_pkey_verify));
		r = 1;
		goto exit;
	}

	// Test failed retrieval of signed dynamic application data
	memset(&ssad, 0, sizeof(ssad));
	r = emv_rsa_retrieve_sdad(
		invalid_enc_sdad,
		sizeof(invalid_enc_sdad),
		&icc_pkey,
		ddol_data,
		sizeof(ddol_data),
		&sdad
	);
	if (r >= 0) {
		fprintf(stderr, "Unexpected emv_rsa_retrieve_sdad() result; r=%d\n", r);
		r = 1;
		goto exit;
	}

	// Test retrieval of signed dynamic application data with object hash validation
	memset(&ssad, 0, sizeof(ssad));
	r = emv_rsa_retrieve_sdad(
		valid_enc_sdad,
		sizeof(valid_enc_sdad),
		&icc_pkey,
		ddol_data,
		sizeof(ddol_data),
		&sdad
	);
	if (r) {
		fprintf(stderr, "emv_rsa_retrieve_sdad() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&sdad, &retrieved_sdad, sizeof(retrieved_sdad)) != 0) {
		fprintf(stderr, "Retrieved combined application data signature is incorrect\n");
		print_buf("retrieved", &sdad, sizeof(sdad));
		print_buf("expected", &retrieved_sdad, sizeof(retrieved_sdad));
		r = 1;
		goto exit;
	}

	printf("Success\n");
	r = 0;
	goto exit;

exit:
	emv_tlv_list_clear(&valid_icc);
	emv_tlv_list_clear(&valid_params);
	return r;
}
